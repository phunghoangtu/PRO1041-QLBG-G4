    private void fillConBan() {
        try {
            List<SanPham> conban = SanPhamRP.getConBan(dfkw);
            modelSPConBan.setRowCount(0);
            for(SanPham sp: conban){
                modelSPConBan.addRow(new Object[]{
                    sp.getMasanpham(),
                    sp.getTensanpham(),
                    sp.getGiaban(),
                    sp.getSoluong(),
                    sp.getMota(),
                    sp.getIdkichcogiay(),
                    sp.getIdmausac(),
                    sp.getIdchatlieugiay(),
                    sp.getIdthuonghieu()
                });
            }
        } catch (Exception e) {
        }
    }
    
